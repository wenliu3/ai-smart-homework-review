"""查重统一配置 —— 文本+图片双维度共用一份配置，改一处生效。

所有阈值、权重、上限集中放这里，老师想调"30%改成25%"之类的，
一个地方改完两个维度都生效，不用满目录翻文件。
"""

# ===================== 通用阈值 =====================
PASS_RATE = 30              # 合格阈值(%)：综合重复率超过该值判定为"疑似抄袭"
MAX_SUBMISSIONS = 200       # 单次查重最大比对数量，防止 O(n²) 导致长时间阻塞

# ===================== 文本维度 =====================
PHRASE_NGRAM = 10           # 片段重合度字符窗口长度
PHRASE_WEIGHT = 0.6         # 片段重合度权重（默认，前端可覆盖）
TOPIC_WEIGHT = 0.4          # 主题相似度权重（默认，前端可覆盖）
# 综合重复率 = 片段重合度 × PHRASE_WEIGHT + 主题相似度 × TOPIC_WEIGHT
# 权重可由前端动态调整，不传时使用这里的默认值
MIN_VALID_CHARS = 40        # 有效字符数少于该值视为无效作业
COMMON_NGRAM_RATIO = 0.6    # n-gram 出现在 >= 60% 提交中即视为公共模板内容
COMMON_NGRAM_MIN_DOCS = 5   # 提交数 >= 5 份才启用"自动公共片段识别"
SNIPPET_LIMIT = 10          # 返回给前端复核的命中片段样例最大数量

# ===================== 图片维度 =====================
HASH_SIZE = 8                # 缩略图边长，8 -> 64位哈希
DISTANCE_THRESHOLD = 8      # 汉明距离(64位里)，<= 此值判定为"疑似同一张图"
MAX_IMAGES_PER_SUBMISSION = 15  # 每份提交最多比较的图片数量
